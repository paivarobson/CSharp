
--CREATE DATABASE LOCACAO

CREATE TABLE PESSOA (
	PESCOD INT PRIMARY KEY NOT NULL,
	PESCPF VARCHAR(11) NULL,
	PESNOM VARCHAR(50) NULL,
	PESTIP CHAR NULL,
)

CREATE TABLE LOCACAO (
	LOCCOD INT PRIMARY KEY NOT NULL,
	VEICOD INT NULL,
	CLICOD INT NULL,
	LOCVLR DECIMAL NULL,
	LOCDAT DATETIME NULL,
	LOCDATDEV DATETIME NULL,
	FOREIGN KEY (CLICOD) REFERENCES PESSOA(PESCOD)
)

CREATE TABLE VEICULO (
	VEICOD INT PRIMARY KEY NOT NULL,
	VEIDES VARCHAR(50) NULL,
	VEIPLC VARCHAR(7) NULL,
	PRPCOD INT NULL,
	LOCCOD INT NULL,
	FOREIGN KEY (PRPCOD) REFERENCES PESSOA(PESCOD),
	FOREIGN KEY (LOCCOD) REFERENCES LOCACAO(LOCCOD)
)
